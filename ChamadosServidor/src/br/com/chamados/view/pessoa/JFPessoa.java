    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        try {
            Cidade cidade = new Cidade();
            cidade.setId(((ItemCombo) jcCidade.getSelectedItem()).getCodigo());
            Pessoa pessoa = new Pessoa();
            if (!jtCampo.getText().equals(PessoaDao.proximoId())) {
                pessoa.setId(Integer.parseInt(jtCampo.getText()));
            }
            pessoa.setNome(jtNome.getText());
            pessoa.setIdade(Integer.parseInt(jtIdade.getText()));
            pessoa.setCpf(jtCpf.getText());
            pessoa.setRg(jtRg.getText());
            pessoa.setEmail(jtEmail.getText());
            pessoa.setTelefone(jtTelefone.getText());
            String data = jftDataNasc.getText();
            SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
            try {
                Date d = format.parse(data);
                pessoa.setDataNascimento(d);
            } catch (ParseException ex) {
                java.util.logging.Logger.getLogger(JFPessoa.class.getName()).log(Level.SEVERE, null, ex);
            }
            pessoa.setCidade(cidade);
            if (Campos.validar(jpPessoa)) {
                PessoaDao.save(pessoa);
                DesabilitaCampos.run(jpPessoa);
                LimparCampos.run(jpPessoa);
            }
            jbSalvar.setEnabled(false);
        } catch (HibernateException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao salvar");
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

