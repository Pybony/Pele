    public void vaPara(String vaPara) {
        if (vaPara.equals("")) {
            vaPara = JOptionPane.showInputDialog("Vá para");
            if (vaPara == null || vaPara.equals("")) {
                vaPara = "0";
            }
        }
        pessoa = PessoaDao.vaPara(vaPara);
        if (pessoa != null) {

            jtCampo.setText(String.valueOf(pessoa.getId()));
            jtNome.setText(pessoa.getNome());
            jtIdade.setText(String.valueOf(pessoa.getIdade()));
            jtTelefone.setText(pessoa.getTelefone());
            jtCpf.setText(pessoa.getCpf());
            jtRg.setText(pessoa.getRg());
            jtEmail.setText(pessoa.getEmail());

            SimpleDateFormat format = new SimpleDateFormat("dd/MM/yyyy");
            Date y = pessoa.getDataNascimento();
            jftDataNasc.setText(format.format(y));

            ItemCombo uf = new ItemCombo();
            Cidade cidade = CidadeDao.getEstadoId(pessoa.getCidade().getId());
            uf.setCodigo(cidade.getEstado().getId());
            new CombosDAO().definirItemCombo(jcEstado, uf);

            jcCidade.setEnabled(true);
            ItemCombo c = new ItemCombo();
            c.setCodigo(cidade.getId());
            new CombosDAO().definirItemCombo(jcCidade, c);

        } else {
            Campos.limpar(jpPessoa);
            Campos.desabilitar(jpPessoa);
        }
    }

    @Override
    public void pesquisar() {
        if (iFPessoa == null) {
            iFPessoa = new IfPessoa(this);
        }
        PessoaDao.popularTabela(iFPessoa.getTabela(), null);
        iFPessoa.setModal(true);
        iFPessoa.setVisible(true);
        jbSalvar.setEnabled(true);
    }

