    private void selecionarInteracao() {
        int interacaoId = (int) jtComentario.getValueAt(jtComentario.getSelectedRow(), 0);
        Interacao bInteracao = new Interacao();
        bInteracao.setId(interacaoId);
        Interacao interacao = InteracaoDao.obterInteracao(bInteracao).get(0);
        mostrarInteracao(interacao);
    }

    private void mostrarInteracao(Interacao interacao) {
        jdMostrarInteracao = new JdMostrarInteracao(jfMenu, true, interacao);
        jdMostrarInteracao.setVisible(true);
        jdMostrarInteracao.toFront();
    }
