    private void salvarComentario(){
        String descricao = "";
        String quem = Cookies.usuario.getFuncionario().getPessoa().getNome();
        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/YYYY");
        Date date = new Date();
        String quando = formatter.format(date);
        
        descricao += "Chamado " + chamadoComentario.getId() + " " + chamadoComentario.getTitulo() + " \n";
        descricao += "Comentário adicionado por " + quem + " em " + quando + " \n";
        descricao += jtAdicionarComentario.getText() + "\n\n\n";
        
        Interacao interacao = new Interacao();
        interacao.setFuncionario(Cookies.usuario.getFuncionario());
        interacao.setChamado(chamadoComentario);
        interacao.setData(date);
        interacao.setDescricao(descricao);
        
        InteracaoDao.salvar(interacao);
        popularTabela();
        
    }
    
    private void popularTabela(){
        Interacao interacaoTabela = new Interacao();
        interacaoTabela.setChamado(chamadoComentario);
        InteracaoDao.popularTabela(jtComentario, interacaoTabela);
    }

